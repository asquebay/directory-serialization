// код в этом файле является портом guess_ja.cpp из исходников KDE/Kate
// он использует конечные автоматы (DFA) для определения японских кодировок
// (см. https://api.kde.org/legacy/4.14-api/kdelibs-apidocs/kdecore/html/guess__ja_8cpp_source.html)

type dfaTable [256]int8

type guessArc struct {
	next  int8
	score float64
}

type guessDFA struct {
	states []dfaTable
	arcs   []guessArc
	state  int8
	score  float64
}

func newGuessDFA(states []dfaTable, arcs []guessArc) *guessDFA {
	return &guessDFA{
		states: states,
		arcs:   arcs,
		state:  0,
		score:  1.0,
	}
}

func (d *guessDFA) next(ch byte) {
	if d.state >= 0 {
		arc := d.states[d.state][ch]
		if arc < 0 {
			d.state = -1
		} else {
			d.state = d.arcs[arc].next
			d.score *= d.arcs[arc].score
		}
	}
}

func (d *guessDFA) isAlive() bool {
	return d.state >= 0
}

// JapaneseCodeType представляет тип японской кодировки
type JapaneseCodeType int

const (
	JapaneseCodeASCII JapaneseCodeType = iota
	JapaneseCodeJIS
	JapaneseCodeEUC
	JapaneseCodeSJIS
	JapaneseCodeUTF8
)

type japaneseCode struct {
	eucj          *guessDFA
	sjis          *guessDFA
	utf8          *guessDFA
	lastJISEscape bool
}

func newJapaneseCode() *japaneseCode {
	return &japaneseCode{
		eucj: newGuessDFA(guessEucjSt, guessEucjAr),
		sjis: newGuessDFA(guessSjisSt, guessSjisAr),
		utf8: newGuessDFA(guessUtf8St, guessUtf8Ar),
	}
}

func (kc *japaneseCode) guessJP(buf []byte) JapaneseCodeType {
	buflen := len(buf)
	for i := 0; i < buflen; i++ {
		c := buf[i]

		// special treatment of jis escape sequence
		if c == 0x1b || kc.lastJISEscape {
			if i < buflen-1 {
				if kc.lastJISEscape {
					c = buf[i]
				} else {
					i++
					c = buf[i]
				}
				kc.lastJISEscape = false
				if c == '$' || c == '(' {
					return JapaneseCodeJIS
				}
			} else {
				kc.lastJISEscape = true
			}
		}

		if kc.eucj.isAlive() {
			if !kc.sjis.isAlive() && !kc.utf8.isAlive() {
				return JapaneseCodeEUC
			}
			kc.eucj.next(c)
		}

		if kc.sjis.isAlive() {
			if !kc.eucj.isAlive() && !kc.utf8.isAlive() {
				return JapaneseCodeSJIS
			}
			kc.sjis.next(c)
		}

		if kc.utf8.isAlive() {
			if !kc.sjis.isAlive() && !kc.eucj.isAlive() {
				return JapaneseCodeUTF8
			}
			kc.utf8.next(c)
		}

		if !kc.eucj.isAlive() && !kc.sjis.isAlive() && !kc.utf8.isAlive() {
			return JapaneseCodeASCII
		}
	}

	if kc.eucj.score == 1.0 && kc.sjis.score == 1.0 && kc.utf8.score == 1.0 {
		return JapaneseCodeASCII
	}

	var top *guessDFA
	if kc.eucj.isAlive() {
		top = kc.eucj
	}
	if kc.utf8.isAlive() {
		if top != nil {
			if top.score < kc.utf8.score {
				top = kc.utf8
			}
		} else {
			top = kc.utf8
		}
	}
	if kc.sjis.isAlive() {
		if top != nil {
			if top.score <= kc.sjis.score {
				top = kc.sjis
			}
		} else {
			top = kc.sjis
		}
	}

	switch {
	case top == kc.eucj:
		return JapaneseCodeEUC
	case top == kc.utf8:
		return JapaneseCodeUTF8
	case top == kc.sjis:
		return JapaneseCodeSJIS
	}

	return JapaneseCodeASCII
}

